                    <p>গত মঙ্গলবার (২৩শে আগস্ট, ২০২২) বাংলাদেশ ব্যাংকের পেমেন্ট সিস্টেম্স ডিপার্টমেন্ট (PSD) থেকে এ সংক্রান্ত একটি সার্কুলার জারি করা হয়। দেশের সকল তফসিলি ব্যাংক, মোবাইল ফিন্যান্সিয়াল সার্ভিসেস প্রোভাইডার, পেমেন্ট সিস্টেম অপারেটর ও পেমেন্ট সার্ভিস প্রোভাইডারদের ব্যবস্থাপনা পরিচালক/প্রধান নির্বাহীদের নিকট জারীকৃত সার্কুলারটি পাঠানো হয়েছে।</p>

                    <p>পেমেন্ট সিস্টেম্স ডিপার্টমেন্ট (PSD) এর পরিচালক কর্তৃক স্বাক্ষরিত উক্ত সার্কুলারটিতে বলা হয়েছে- “বাংলাদেশ ব্যাংক অর্ডার ১৯৭২ এর ৭(এ)(ই) ধারার ক্ষমতাবলে জারীকৃত “বাংলাদেশ পেমেন্ট অ্যান্ড সেটেলমেন্ট সিস্টেম্স রেগুলেশন-২০১৪” অনুসারে বাংলাদেশ ব্যাংক কর্তৃক “অপটিমাম সল্যুশন এন্ড সার্ভিসেস লিমিটেড”-কে অত্র বিভাগের পিএসডি/এডিসি এন্ড এল(ওএসএসএল)/২০/২০২২-১৯০২ নং পত্রে বর্ণিত শর্তসাপেক্ষে দেশের অভ্যন্তরে “Easy Payment System (EPS)” ব্র্যান্ড নামে পেমেন্ট সিস্টেম অপারেটর (PSO) হিসেবে লাইসেন্স প্রদান করা হয়েছে।”</p>

                    <p>উল্লেক্ষ্য যে, বাংলাদেশ ব্যাংকের পেমেন্ট সিস্টেম্স ডিপার্টমেন্ট (PSD) এখন পর্যন্ত মোট সাতটি প্রতিষ্ঠানকে পেমেন্ট সিস্টেম অপারেটর (PSO) হিসেবে লাইসেন্স প্রাদান করেছে। ইপিএস তাদের মধ্যে একটি। এই অনুমোদনের মাধ্যমে, ইপিএস ব্যক্তি এবং ব্যবসা প্রতিষ্ঠানগুলোকে আর্থিক লেনদেন সংক্রান্ত পরিষেবা প্রদান করতে সক্ষম।
